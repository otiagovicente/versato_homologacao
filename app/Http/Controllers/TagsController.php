    public function api_selectList($brand_id){

        $tags = Tag::where('brand_id', $brand_id)->get();

        foreach($tags as $tag){

            $selectItem['value'] = $tag->id;
            $selectItem['label'] = $tag->description;
            $selectList[] = $selectItem;
        }

        return response()->json($selectList);

